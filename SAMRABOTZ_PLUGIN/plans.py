        f"рҹҶ” рқ•Ңрқ•Ҡрқ”јв„қ рқ•Җрқ”»: <code>{user_id}</code>\n"
        f"вӯҗ рқ”ёв„Ӯв„Ӯрқ•Ҷрқ•Ңв„•рқ•Ӣ: {status}\n"
