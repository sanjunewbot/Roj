    user = await db.get_user(user_id)
    if not user: return
    status = "рқ•Қрқ•Җв„ҷ в„ҷв„қрқ”јрқ•„рқ•Җрқ•Ңрқ•„" if user.get('is_premium') else "рқ•Ҡрқ•Ӣрқ”ёв„•рқ”»рқ”ёв„қрқ”» рқ”Ҫв„қрқ”јрқ”ј"
